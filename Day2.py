# Variables
name = "Rithin"
age = 22

print("Name:", name)
print("Age:", age)

# Data Types
print("\nData Types")
print(type(name))
print(type(age))

# Operators
a = 20
b = 10

print("\nOperators")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# For Loop
print("\nFor Loop")
for i in range(1, 6):
    print(i)

# While Loop
print("\nWhile Loop")
count = 1
while count <= 5:
    print(count)
    count += 1

# Function
def greet(name):
    print("\nHello,", name + "! Welcome to Python.")

greet("Rithin")